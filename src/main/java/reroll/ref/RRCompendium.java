package reroll.ref;

public class RRCompendium {
	private RRBackground[] backgrounds;
	private RRClass[] classes;
	private RRFeat[] feats;
	private RRRace[] races;
	private RRSpell[] spells;
	private RRSubClass[] subclasses;
	
	public RRBackground[] getBackgrounds() {
		return backgrounds;
	}
	public void setBackgrounds(RRBackground[] backgrounds) {
		this.backgrounds = backgrounds;
	}
	public RRClass[] getClasses() {
		return classes;
	}
	public void setClasses(RRClass[] classes) {
		this.classes = classes;
	}
	public RRFeat[] getFeats() {
		return feats;
	}
	public void setFeats(RRFeat[] feats) {
		this.feats = feats;
	}
	public RRRace[] getRaces() {
		return races;
	}
	public void setRaces(RRRace[] races) {
		this.races = races;
	}
	public RRSpell[] getSpells() {
		return spells;
	}
	public void setSpells(RRSpell[] spells) {
		this.spells = spells;
	}
	public RRSubClass[] getSubclasses() {
		return subclasses;
	}
	public void setSubclasses(RRSubClass[] subclasses) {
		this.subclasses = subclasses;
	}
}
